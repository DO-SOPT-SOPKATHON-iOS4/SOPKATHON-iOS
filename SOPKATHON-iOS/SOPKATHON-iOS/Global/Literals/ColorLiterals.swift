import UIKit

extension UIColor {
    static var SOPTBlack: UIColor {
        return UIColor(hex: "#000000")
    }
    
    static var SOPTGrey800: UIColor {
        return UIColor(hex: "#434343")
    }
    
    static var SOPTGrey600: UIColor {
        return UIColor(hex: "#6F6F6F")
    }
    
    static var SOPTGrey400: UIColor {
        return UIColor(hex: "#B7B7B7")
    }
    
    static var SOPTGrey200: UIColor {
        return UIColor(hex: "#EBEBEB")
    }
    
    static var SOPTWhite: UIColor {
        return UIColor(hex: "#FFFFFF")
    }
    
    static var SOPTYellow: UIColor {
        return UIColor(hex: "#FFD600")
    }
    
    static var SOPTOrange: UIColor {
        return UIColor(hex: "#FF8339")
    }
    
    static var SOPTBlue: UIColor {
        return UIColor(hex: "#7187FF")
    }
    
    static var SOPTGreen: UIColor {
        return UIColor(hex: "#1FD068")
    }
    
    static var SOPTRed: UIColor {
        return UIColor(hex: "#FE5D5C")
    }
}
